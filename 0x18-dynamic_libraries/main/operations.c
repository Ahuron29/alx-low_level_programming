#include <stdio.h>
#include "operators.h"
/**
 * add the two integers
 * @a: first int
 * @b: second int
 * Return result
 */
int add(int a, int b)
{
  return (a + b);
}
/**
 * subtract the two integers
 * @a: first int
 * @b: second int
 * Return the result
 */
int sub(int a, int b)
{
  return (a - b);
}
/**
 * multiply the two integers
 * @a: first int
 * @b: second int
 * Return the result
 */
int mul(int a, int b)
{
  return (a * b);
}
/**
 * divide the two integers
 * @a: first int
 * @b: second int
 * Return the result
 */
int div(int a, int b)
{
  return (a / b);
}
/**
 * Modulus of the two integers
 * @a: first int
 * @v: second int
 * Return the result
 */
int mod(int a, int b)
{
  return (a % b);
}
